    function displayLocations(vehicle) {
        locationsContainer.innerHTML = '';
        const locations = locationsByVehicle[vehicle] || [{ location: "No locations available", url: "#" }];
        locations.forEach(item => {
            const locationElement = document.createElement('div');
            locationElement.className = 'location-item';
            const locationLink = document.createElement('a');
            locationLink.href = item.url;
            locationLink.textContent = `${vehicle} ${item.location}`;
            locationLink.target = '_blank';
            locationElement.appendChild(locationLink);
            locationsContainer.appendChild(locationElement);
        });
        locationsDropdown.style.display = 'block';
        locationsDropdown.setAttribute('aria-hidden', 'false');
        // Calculate the bottom of the dropdown
        const dropdownRect = locationsDropdown.getBoundingClientRect();
