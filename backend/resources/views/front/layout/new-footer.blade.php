        // If the dropdown extends beyond the viewport, scroll to show it entirely
        if (dropdownBottom > window.innerHeight) {
            const scrollAmount = dropdownBottom - window.innerHeight + 20; // 20px extra for padding
            window.scrollBy({
                top: scrollAmount,
                behavior: 'smooth'
            });
        }
