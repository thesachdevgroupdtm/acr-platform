<script>
// Sidebar Form Captcha Handler
class SidebarCaptcha {
    constructor() {
        this.formId = 'sidebarForm';
        this.questionId = 'sidebar-captcha-question';
        this.answerId = 'sidebar-captcha-answer';
        this.correctAnswerId = 'sidebar-correct-answer';
        this.init();
    }
    init() {
        console.log('Initializing Sidebar Captcha...');
        // Wait for DOM to be ready
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => this.setup());
        } else {
            this.setup();
        }
    }
    setup() {
        console.log('Setting up sidebar captcha...');
        // Generate captcha immediately
        this.generateCaptcha();
        // Setup form submission handler
        const form = document.getElementById(this.formId);
        if (form) {
            form.addEventListener('submit', (e) => this.handleSubmit(e));
            console.log('Sidebar form handler attached');
        } else {
            console.error('Sidebar form not found');
        }
    }
    generateCaptcha() {
        console.log('Generating sidebar captcha...');
        const questionEl = document.getElementById(this.questionId);
        const correctAnswerEl = document.getElementById(this.correctAnswerId);
        const answerEl = document.getElementById(this.answerId);
        if (!questionEl || !correctAnswerEl) {
            console.error('Captcha elements not found');
            return;
        }
        // Generate simple math problem
        const num1 = Math.floor(Math.random() * 10) + 1;
        const num2 = Math.floor(Math.random() * 10) + 1;
        const operators = ['+', '-'];
        const operator = operators[Math.floor(Math.random() * operators.length)];
        let question, answer;
        if (operator === '+') {
            question = `${num1} + ${num2}`;
            answer = num1 + num2;
        } else {
            if (num1 < num2) {
                question = `${num2} - ${num1}`;
                answer = num2 - num1;
            } else {
                question = `${num1} - ${num2}`;
                answer = num1 - num2;
            }
        }
        questionEl.textContent = `${question} = ?`;
        correctAnswerEl.value = answer;
        if (answerEl) {
            answerEl.value = '';
        }
        console.log(`Sidebar captcha generated: ${question} = ${answer}`);
    }
    validateCaptcha() {
        const userAnswer = parseInt(document.getElementById(this.answerId).value);
        const correctAnswer = parseInt(document.getElementById(this.correctAnswerId).value);
        console.log(`Sidebar captcha validation: ${userAnswer} vs ${correctAnswer}`);
        return userAnswer === correctAnswer;
    }
    handleSubmit(event) {
        console.log('Sidebar form submission...');
        // Validate captcha
        if (!this.validateCaptcha()) {
            event.preventDefault();
            alert('Captcha answer is incorrect. Please try again.');
            this.generateCaptcha();
            return false;
        }
        // Validate required fields
        const form = event.target;
        const requiredFields = ['name', 'phone', 'email', 'location'];
        for (let fieldName of requiredFields) {
            const field = form.querySelector(`[name="${fieldName}"]`);
            if (!field || !field.value.trim()) {
                event.preventDefault();
                alert(`${fieldName.charAt(0).toUpperCase() + fieldName.slice(1)} is required.`);
                return false;
            }
        }
        // Check agree checkbox
        const agreeCheckbox = form.querySelector('[name="agree"]');
        if (!agreeCheckbox || !agreeCheckbox.checked) {
            event.preventDefault();
            alert('You must agree to the terms and conditions.');
            return false;
        }
        console.log('Sidebar form validation passed, submitting...');
        return true;
    }
}
