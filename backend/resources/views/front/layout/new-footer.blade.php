    function selectVehicleType(vehicle, element) {
        const wasActive = element.classList.contains('active');
        document.querySelectorAll('.vehicle-type').forEach(el => el.classList.remove('active'));
        if (!wasActive) {
            element.classList.add('active');
            displayLocations(vehicle);
        } else {
            closeDropdown();
        }
